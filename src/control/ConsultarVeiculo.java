	private void btExcluirActionPerformed( java.awt.event.ActionEvent evt ) {
		try {
			Connection connection = new ConnectionFactory().getConnection();
			String sql = "delete from veiculo where placa = ?";
			PreparedStatement statement = connection.prepareStatement( sql );
			int linha = this.jtVeiculo.getSelectedRow();
			statement.setString( 1, jtVeiculo.getValueAt( linha, 0 ).toString() );
			statement.execute();
			statement.close();
			connection.close();
			DefaultTableModel model = ( DefaultTableModel ) jtVeiculo.getModel();
			model.removeRow( linha );
			JOptionPane.showMessageDialog( this, "Veiculo Exclu√≠do com Sucesso!" );
			this.setClosable( true );
		} catch( SQLException e ) {
			JOptionPane.showMessageDialog( this, e );
		}
	}



